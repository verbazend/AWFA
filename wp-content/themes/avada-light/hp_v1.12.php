Do you have your USI yet?<br>
From 1<sup>st</sup> January 2015, students enrolling in nationally recognised training in Australia will need a Unique Student Identifier (USI).<br><br>
The exclusive 10 digit number consisting of letters and numbers will be used to link a student's USI account to their training records.<br>It will look something like this: 3AW88YH9U5.<br><br>
Obtaining a USI is <u><b>FREE</b></u> and easy for students.<br>
To create your own <u>USI Online</u> and to access information and fact sheets go to: <a href="http://www.usi.gov.au" target="_blank">www.usi.gov.au</a><br><br>
<b>NB. Under the legislation, training organisations can only issue certification when they have received your USI</b>

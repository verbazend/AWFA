<h1 style="text-align: center; font-size: 30px !important;">Australia Wide First Aid</h1>
<p style="text-align: center; font-size: 16px !important; margin-top: -10px; line-height: 24px;">Do you know what to do in an emergency? Is there at least one person in your home with <strong><span style="color: #ce1443;">CPR or first aid knowledge?</span></strong> Could you confidently give CPR or care for an injured person until help arrives?</p>
[separator top="50" style="single"]

[one_half last="no"]As Australia’s leader in innovative first aid solutions, <b>Australia Wide First Aid</b> prides itself on providing simple, streamlined first aid training solutions. The one-day Provide First Aid course, will equip you with necessary first aid knowledge, skills and reference materials, in addition to a nationally recognised certificate of attainment.

First aid training is highly valued by employees and in many cases, is a requirement for employment within Australia. Australia Wide First Aid conducts courses seven days a week (locations specific), at a range of times to ensure optimum convenience for those looking to complete a course. <b>Same day certificates</b> are provided, as long as payment has been received prior to course commencement.
[/one_half]

[one_half last="yes"]Australia Wide First Aid delivers the Provide First Aid course in one day.  All course participants will receive access to our <strong>first aid manual</strong> upon booking, to ensure up-to-date information is immediately available to you. Australia Wide First Aid also provides access to the renewal program, ensuring first aid certification is always up to date.

If your workplace requires training for numerous employees, Australia Wide First Aid <b>trainers can come to you</b>. Australia Wide First Aid’s Group Booking service allows employees to maintain current certification with minimal interruption to business activity. Complete our <a href="/contact/#enquire">Group Booking Enquiry Form</a> or call our friendly staff on 1300 336 613 between 8:00am – 6:00pm to secure your booking.
[/one_half]
